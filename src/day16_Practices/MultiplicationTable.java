package day16_Practices;

public class MultiplicationTable {

    public static void main(String[] args) {


        for (int i = 1; i < 11; i++) {
            System.out.println();
            for (int j = 1; j < 11; j++) {
                System.out.print(i*j +"\t\t");

            }
        }

    }
}
